# operator assigment merupakan operator penugasan

# = 
# bertugas untuk memasukkan value ke dalam variabel
a = 2

# +=
# bertugas untuk menambahkan nilai kiri dengan nilai kanan
a += 3
print(a)

# -=
# bertugas untuk mengurangi nilai kiri dengan nilai kanan
a -= 1
print(a)

# *=
# bertugas untuk mengkalikan nilai kiri dengan nilai kanan
a *= 9
print(a)

# /=
# bertugas untuk membagi nilai kiri dengan nilai kanan
a /= 2
print(a)

# **=
# bertugas untuk mempangkatkan nilai sebelah kiri dengan nilai kanan
a**= 2
print(a)

# //=
# bertugas untuk membagi nilai kiri dengan nilai kanan hanya saja akan menghasilkan nilai pembagian yang bulat dan akan di bulatkan ke bawah
a//=3
print(a)

# %=
# bertugas untuk mendapatkan hasil sisa bagi dari nilai kiri di bagi dengan nilai kanan
a%=5
print(a)